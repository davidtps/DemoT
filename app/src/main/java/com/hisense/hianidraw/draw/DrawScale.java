public class DrawScale {
    private float scaleX;
    private float scaleY;

    public final float getScaleX() {
        return scaleX;
    }

    public final float getScaleY() {
        return scaleY;
    }

    public DrawScale(float scaleX, float scaleY) {
        this.scaleX = scaleX;
        this.scaleY = scaleY;
    }
}
